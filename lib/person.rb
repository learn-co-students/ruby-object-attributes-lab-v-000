class Person

#setter method assigns a instance variable to the argument pass to it, name
  def name=(name)
    @name = name
  end

#getter method reads or writes the instance variable (@name).
  def name
    @name
  end

#setter method assigns a instance variable to the argument pass to it, job
  def job=(job)
    @job = job
  end
#getter method reads or writes the instance variable (@job).
  def job
    @job
  end

end
