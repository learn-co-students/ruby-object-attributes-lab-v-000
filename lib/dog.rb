class Dog

  def name=(dog_name)     #writes the name of the dog to an instance variable @name
    @name = dog_name
  end

  def name                #reads the name of the dog from an instance variable @name
    @name
  end

  def breed=(dog_breed)   #writes the breed of the dog to an instance variable @breed
    @breed = dog_breed
  end

  def breed               #reads the breed of the dog from an instance variable @breed
    @breed
  end

end
