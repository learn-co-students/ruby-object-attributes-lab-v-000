class Dog
  def name
    #reads name of the dog from instance variable @name
    @name
  end
  def name= dog_name
    #writes name of dog to an instance variable @name
    @name = dog_name
  end
  def breed
    #reads breed
    @breed
  end
  def breed= dog_breed
    #writes breed
    @breed = dog_breed
  end
end
