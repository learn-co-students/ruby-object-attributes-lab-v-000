class Dog
  def name=(name) #writes the name of the dog to an instance variable @name
    @name = name
  end

  def name #reads the name of the dog from an instance variable @name
    @name
  end

  def breed=(breed) #writes the breed of the dog to an instance variable @breed
    @breed = breed
  end

  def breed #reads the breed of the dog from an instance variable @breed
    @breed
  end
end
